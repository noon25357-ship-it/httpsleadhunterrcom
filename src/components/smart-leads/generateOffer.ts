import type { SmartLead } from './types';

export function generateOfferMessage(lead: SmartLead): string {
  if (lead.websiteStatus === 'no-website') {
    return `هلا، شفت نشاطكم ${lead.businessName} وحبيت أقولك شيء مهم:

فيه ناس كثير يدورون في قوقل على خدمات مثل اللي تقدمونها، ولما ما يلقون لكم موقع واضح ومرتب غالبًا يروحون لمنافس ثاني.

أنا أقدر أبني لكم موقع احترافي يوضح خدماتكم ويعطيكم حضور أفضل وثقة أعلى قدام العملاء.

إذا مناسب لك أرسل لك تصور سريع 🚀`;
  }

  if (lead.websiteStatus === 'weak-website') {
    return `هلا، شفت موقع ${lead.businessName} وحبيت ألفت نظرك لشيء مهم:

كثير من العملاء يدخلون الموقع أول مرة، وإذا كان بطيء أو غير مريح على الجوال غالبًا يطلعون بسرعة ويروحون لغيركم.

أقدر أساعدكم في تطوير موقع احترافي أسرع وأوضح ويخدمكم بشكل أفضل في جلب العملاء.

إذا حاب أرسل لك تصور سريع 🚀`;
  }

  return `هلا، شفت نشاطكم ${lead.businessName} وعجبني حضوركم الرقمي.

عندي أفكار ممكن تساعدكم تطورون موقعكم أكثر وتجيبون عملاء جدد من قوقل.

إذا يهمكم أرسل لكم اقتراح سريع 🚀`;
}
